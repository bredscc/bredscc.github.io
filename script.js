function setLanguage(lang) {
    const selectedTranslations = translations[lang] || translations['en']; 

    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (selectedTranslations[key]) {
          element.textContent = selectedTranslations[key];
        }
    });

    document.querySelectorAll('.nav-links a').forEach(a => {
        const key = a.getAttribute('data-key');
        if (selectedTranslations[key]) {
            a.setAttribute('aria-label', selectedTranslations[key]);
        }
    });
}


document.addEventListener('DOMContentLoaded', () => {
    const langSelector = document.getElementById("language-selector");

    let currentLang = localStorage.getItem('language');
    if (!currentLang) {
        const browserLang = navigator.language.substring(0, 2);
        currentLang = translations[browserLang] ? browserLang : 'en';
    }

    if (langSelector) {
        langSelector.value = currentLang;
        langSelector.addEventListener('change', (e) => {
            const newLang = e.target.value;
            localStorage.setItem('language', newLang);
            setLanguage(newLang);
        });
    }

    setLanguage(currentLang);
    const canvas = document.getElementById("network-canvas");
    if (canvas) {
        const ctx = canvas.getContext("2d");
        const resizeCanvas = () => {
            canvas.width = window.innerWidth;
            canvas.height = document.body.scrollHeight; 
        };

        let particles = [];
        const maxDistance = 120;
        const particleCount = 120;

        class Particle {
            constructor(x, y) {
                this.x = x;
                this.y = y;
               
                this.vx = (Math.random() - 0.5) * 0.5; 
                this.vy = (Math.random() - 0.5) * 0.5;
                this.radius = 1.2;
            }
            update() {
                this.x += this.vx;
                this.y += this.vy;
                
                if (this.x < 0 || this.x > canvas.width) this.vx *= -1;
                if (this.y < 0 || this.y > canvas.height) this.vy *= -1;
            }
            draw() {
                ctx.beginPath();
                ctx.arc(this.x, this.y, this.radius, 0, Math.PI * 2);
                ctx.fillStyle = "rgba(100, 200, 200, 0.8)";
                ctx.fill();
            }
        }

        function connectParticles() {
            for (let a = 0; a < particles.length; a++) {
                for (let b = a + 1; b < particles.length; b++) {
                    const dx = particles[a].x - particles[b].x;
                    const dy = particles[a].y - particles[b].y;
                    const distance = Math.hypot(dx, dy); 

                    if (distance < maxDistance) {
                        const opacity = 1 - distance / maxDistance; 
                        ctx.beginPath();
                        ctx.strokeStyle = `rgba(100, 200, 200, ${opacity * 0.4})`; 
                        ctx.lineWidth = 1;
                        ctx.moveTo(particles[a].x, particles[a].y);
                        ctx.lineTo(particles[b].x, particles[b].y);
                        ctx.stroke();
                    }
                }
            }
        }

        function initParticles() {
            particles = [];
            for (let i = 0; i < particleCount; i++) {
                particles.push(new Particle(Math.random() * canvas.width, Math.random() * canvas.height));
            }
        }

        function animate() {
            ctx.clearRect(0, 0, canvas.width, canvas.height);
            particles.forEach((p) => {
                p.update();
                p.draw();
            });
            connectParticles();
            requestAnimationFrame(animate);
        }

        window.addEventListener("resize", () => {
            resizeCanvas();
            initParticles();
        });

        resizeCanvas();
        initParticles();
        animate();
    }
});